package com.comp.tracker.dto;

public class Storage {
	
	private String assessionID;
	private String sampleID;
	private String refTemp;
	private String storageLocationID;
	private String storageDate;
	private String storageTime;
	private String siteID;
	public String getAssessionID() {
		return assessionID;
	}
	public void setAssessionID(String assessionID) {
		this.assessionID = assessionID;
	}
	public String getSampleID() {
		return sampleID;
	}
	public void setSampleID(String sampleID) {
		this.sampleID = sampleID;
	}
	public String getRefTemp() {
		return refTemp;
	}
	public void setRefTemp(String refTemp) {
		this.refTemp = refTemp;
	}
	public String getStorageLocationID() {
		return storageLocationID;
	}
	public void setStorageLocationID(String storageLocationID) {
		this.storageLocationID = storageLocationID;
	}
	public String getStorageDate() {
		return storageDate;
	}
	public void setStorageDate(String storageDate) {
		this.storageDate = storageDate;
	}
	public String getStorageTime() {
		return storageTime;
	}
	public void setStorageTime(String storageTime) {
		this.storageTime = storageTime;
	}
	public String getSiteID() {
		return siteID;
	}
	public void setSiteID(String siteID) {
		this.siteID = siteID;
	}
	
	

}
